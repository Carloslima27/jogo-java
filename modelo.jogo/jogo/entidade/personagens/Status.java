package jogo.entidade.personagens;

public class Status {
	private Double forca;
	private Double vida;
	
	public Status() {
		forca = 0.0;
		vida = 0.0;
	}
	
	public Double getForca() {
		return forca;
	}

	public void setForca(Double forca) {
		this.forca = forca;
	}

	public Double getVida() {
		return vida;
	}

	public void setVida(Double vida) {
		this.vida = vida;
	}

	
	
	
	

}
